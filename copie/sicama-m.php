</div>
<br/>
<div class="tableauDeBord">
    <div>
        <h4 class="sublimSurligne">Préfinancement des intrants et travaux</h4>
        <br/>
        <p>Cette mission s’avère être très importante, car, elle permet de pérenniser la production de la canne et d’une manière plus générale de maintenir de l’agriculture dans notre bassin.
        <br/>A travers celle-ci, il s’agit pour SICAMA de financer les travaux d’entretien (+ de 1 400 ha / an), de plantation (en moyenne 300 ha / an) et d’intrants (boutures, etc).
        <br/>Le préfinancement des travaux est mise en place grâce au concours de la Caisse Régionale du Crédit Agricole Mutuel de la Guadeloupe (CRCAM) qui nous accorde sa confiance.
        <br/>Le budget alloué par SICAMA aux opérations de préfinancement de travaux et d’intrants est de 500 000 € en moyenne / an.
        </p>
    </div>
    <div>
        <h4 class="sublimSurligne">Organisation de la récolte</h4>
        <br/>
        <p>cette mission nous a été confiée récemment (janvier 2014) par l’Interprofession Guadeloupéenne de la Canne (IGUACANNE) via le GIE Canne Guadeloupe. Il s’agit pour l’UDCAG de réaliser les estimations du tas de cannes qui doit être récolté dans l’année et de suivre toute la récolte.</p>
    </div>
    <div>
        <h4 class="sublimSurligne">Redistribution des aides publiques</h4>
        <br/>
        <p>Le reversement des aides publiques se fait via L’UDCAG, qui constitue un relai entre les pouvoirs publics 
            (FEADER, ETAT, ODEADOM, Conseil Régional, Conseil Départemental, FIDOM) 
            et les planteurs du bassin. Chaque année, nous avons recevons pour le compte des planteurs, 
            un montant cumulé de financement d’environ 9 millions d’euros, lequel se réparti sur 3 grandes catégories : 
            Aide à la Garantie de Prix (AGP), Aide à la replantation et aide aux transports. 
            Toutefois, des aides exceptionnelles peuvent être attribuées pour le compte du planteur : aide entretien, etc.
        </p>
    </div>
    <div>
        <h4 class="sublimSurligne">Déclaration graphique annuelle</h4>
        <br/>
        <p>Cette opération est menée en collaboration avec l’Association Guadeloupéenne de Recueil d’Informations Géographiques d’Utilité Agricole (AGRIGUA), 
            qui a pour mission majeure de recenser les différentes parcelles cultivées ou en attente de culture sur le territoire de la Guadeloupe.
            <br/>Elle est réalisée chaque année de Mars à Mai. Ainsi, c’est plus de 800 dossiers qui sont instruits par notre service technique.
            Cette opération permet aux agriculteurs d’être à jour au fichier et de bénéficier les aides publiques.
        </p>
    </div>
    <div>
        <h4 class="sublimSurligne">Conseil, suivi technico-économique et assistance administrative</h4>
        <br/>
        <p>Les planteurs sont accompagnés dans la gestion de leurs exploitations par 4 techniciens. 
            Nous dénombrons en moyenne 1 technicien pour 192 planteurs. 
            Ces derniers sont accompagnés dans l’instruction de leurs demandes de financement plantation, aide aux transports, 
            mesure agro-environnementale, déclaration de surface, etc. Ils sont également accompagnés techniquement dans la conduite de leurs exploitations.</p>
    </div>


